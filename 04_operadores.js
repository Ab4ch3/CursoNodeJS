/* Operadores */

/* Operadores de Asignacion */

let x = 2;
let y = true;

/* Operador de Asignacion de adicion */

let a = 2;
let b = 1;

a += b;
// a = a + b
console.log(a);

/* Operador de Asignacion de resta */
let c = 1;
let d = 2;

c -= d;
console.log(c);

/* Operador de Asignacion de Multiplicacion */
let e = 3;
let f = 4;

e *= f;
console.log(e);

/* Operador de Asignacion de division  o residuo */
let g = 12;
let h = 3;

g /= h;
g %= h;
console.log(g);

/* Operador de Asignacion de exponeciacion  */
let m = 3;
let l = 2;

m **= l;
console.log(m);

/* Operadores de Comparacion */

// Operador igual

console.log(3 == 3);
console.log(3 == "3");

// Operador no es igual
console.log(3 != 3);

// Operador estrictamente igual
console.log(3 === 3);
console.log(3 === "3");

// Operador estrictamente desigual
console.log(3 !== "3");
console.log(3 !== 3);

//Mayor que >, >= ,< , <=
console.log(3 > 4);
console.log(3 >= 4);
console.log(2 < 4);
console.log(2 <= 2);

/* Operadores Aritmeticos */

console.log(2 + 2);
console.log(2 - 2);
console.log(2 * 2);
console.log(2 / 2);
console.log(2 % 2);
console.log(2 ** 3);

/* Operadores Logicos */

//And
console.log(true && true);
console.log(2 > 3 && 1 <= 2);

//OR
console.log(true || true);

//NOT
console.log(!true);
